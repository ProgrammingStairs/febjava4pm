// program showing the concept of Operators
// Relational operator

class Demo3{
    public static void main(String args[]){
        int a = 5,b=2;
        boolean c;
        c = a<b;
        System.out.println(a+" < "+b+" = "+c);
        c = a>b;
        System.out.println(a+" > "+b+" = "+c);
        c = a<=b;
        System.out.println(a+" <= "+b+" = "+c);
        c = a>=b;
        System.out.println(a+" >= "+b+" = "+c);
        c = a==b;
        System.out.println(a+" == "+b+" = "+c);
        c = a!=b;
        System.out.println(a+" != "+b+" = "+c);
    }
}
